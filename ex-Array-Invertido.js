/*Crie a função imprimirInverso que pega um Array como um argumento e
imprime cada elemento em ordem reversa no console (você não precisa
inverter o Array).*/
var lista = ['Maça', 'Banana', 'Abacaxi', 'Laranja']

function imprimirInverso(Listas) {
    console.log(3, Listas[3]);
    console.log(2, Listas[2]);
    console.log(1, Listas[1]);
    console.log(0, Listas[0]);
}

console.log("Lista invertida manual: ");
imprimirInverso(lista);

